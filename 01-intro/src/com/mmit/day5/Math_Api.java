package com.mmit.day5;

public class Math_Api {

	public static void main(String[] args) {
		
		System.out.println("abs value of -7: " + Math.abs(-7));
		System.out.println("Result of 3 Power 4: " + Math.pow(3, 4));
		System.out.println("Result for square root of 81: " + Math.sqrt(81));
		System.out.println("Random value: " + Math.random());
		System.out.println("");
		System.out.println("Min value: " + Math.min(100, 430));
		System.out.println("Rounding number for 3.6: " + Math.round(3.6));
		System.out.println("Floor number for 3.6: " + Math.floor(3.6));
		System.out.println("Ceil for 3.2: " + Math.ceil(3.2));
		System.out.println("Value of PI: " + Math.PI);
		System.out.println("Sin(30): " + Math.sin(30));
		System.out.println("Log10(10): " + Math.log10(10));  
	}

}
